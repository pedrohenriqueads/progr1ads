#4 - Faça um Programa que verifique se uma letra digitada é vogal ou consoante.

letra = input("Digite uma letra: ")
if letra == 'a' :
    print("É uma vogal")
elif letra == 'e' :
    print("É uma vogal")
elif letra == 'i' :
    print("É uma vogal")
elif letra == 'o' :
    print("É uma vogal")
elif letra == 'u' :
    print("É uma vogal")
else:
    print("É uma consoante")
    
